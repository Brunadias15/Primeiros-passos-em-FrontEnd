// Operadores aritméticos
let n1 = 20;
let n2 = 2;

//Adição +
const soma = n1 + n2
console.log("Soma = ", soma)

// Subtração -
console.log("Subtração = ", n1 - n2)

// Multiplicação *
console.log("Multiplicação = ", n1 * n2)

// Divisão /
console.log("Divisão = ", n1 / n2)

// Exponenciação **
console.log("Exponenciação = ", n1 ** n2)

// Resto da divisão %
console.log("Resto da divisão = ", n1 % n2)

//Precendência de operadores
console.log((3 + 7) * 3) //resolve o que está dentro do parenteses e depois o que esta fora, nesse caso primeiro + depois *. Senão segue o podrão da matematica.
console.log((10 + 7 +8) / 3) //Média

//Biblioteca Math - funções de operações
console.log(Math.PI) //Ver o PI
console.log(Math.sqrt(16)) // calcula raiz quadrada
